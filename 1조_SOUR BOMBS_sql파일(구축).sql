CREATE TABLE EMP (
	EMP_ID		CHAR(7)			NOT NULL,
	EMP_NAME	VARCHAR2(10)	NOT NULL,
	HIREDATE	DATE			NULL,
	CONTACT		VARCHAR2(11)	NULL,
	ADDRESS		VARCHAR2(100)	NULL,
	DEPT_ID		NUMBER(4,0)		NOT NULL,
	JOB_ID		NUMBER(4,0)		NOT NULL
);

CREATE TABLE DEPT (
	DEPT_ID		NUMBER(4,0)		NOT NULL,
	DEPT_NAME	VARCHAR2(20)	NULL,
	DEPT_GRADE	NUMBER(4,0)		NULL
);

CREATE TABLE DEPT_HISTORY (
	EMP_ID		CHAR(7)			NOT NULL,
	DEPT_ID		NUMBER(4,0)		NOT NULL,
	DEPT_STARTDATE	DATE		NULL,
	DEPT_ENDDATE	DATE		NULL
);

CREATE TABLE BONUS (
	DEPT_GRADE	NUMBER(1,0)		NULL,
	BONUS_RATE	NUMBER(5,0)		NULL
);

CREATE TABLE SALARY (
	SALARY		NUMBER(10,0)	NULL,
	WORK_EXP	VARCHAR2(20)	NULL
);

CREATE TABLE JOB (
	JOB_ID		NUMBER(4,0)	NOT NULL,
	JOB_NAME	VARCHAR2(20)	NULL
);

CREATE TABLE CERTIFICATE (
	EMP_ID		CHAR(7)		NOT NULL,
	BANCASSURANCE	NUMBER(1,0)	NULL,
	FUND		NUMBER(1,0)		NULL,
	SQLD		NUMBER(1,0)		NULL,
	DERIVATIVES	NUMBER(1,0)		NULL
);

ALTER TABLE EMP ADD CONSTRAINT PK_EMP PRIMARY KEY (
	EMP_ID
);

ALTER TABLE DEPT ADD CONSTRAINT PK_DEPT PRIMARY KEY (
	DEPT_ID
);

ALTER TABLE JOB ADD CONSTRAINT PK_JOB PRIMARY KEY (
	JOB_ID
);

ALTER TABLE EMP ADD CONSTRAINT FK_EMP_1 FOREIGN KEY (
	DEPT_ID
)
REFERENCES DEPT (
	DEPT_ID
);

ALTER TABLE EMP ADD CONSTRAINT FK_EMP_2 FOREIGN KEY (
	JOB_ID
)
REFERENCES JOB (
	JOB_ID
);

ALTER TABLE DEPT_HISTORY ADD CONSTRAINT FK_DHIST_1 FOREIGN KEY (
	DEPT_ID
)
REFERENCES DEPT (
	DEPT_ID
);

ALTER TABLE DEPT_HISTORY ADD CONSTRAINT FK_DHIST_2 FOREIGN KEY (
	EMP_ID
)
REFERENCES EMP (
	EMP_ID
);




